function clickMonth(){
		document.getElementById("weekdays-id").style.display = "block";
		document.getElementById("days-id").style.display = "block";
		document.getElementById("weekly-cal-wrapper-id").style.display = "none";
	}
	

function clickWeek(){
		document.getElementById("weekdays-id").style.display = "none";
		document.getElementById("days-id").style.display = "none";
		document.getElementById("weekly-cal-wrapper-id").style.display = "block";
	}
